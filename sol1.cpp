// Positive or Negative number

#include<iostream>
using namespace std;
 
 
int main(){
 
 int num;
 cin>>num;

 if(num>0){
    cout<<"It is a positive number";
 }

 else if(num==0){
    cout<<"Zero";
 }
 else{
    cout<<"It is a negative number";
 }
  
  
return 0;
}